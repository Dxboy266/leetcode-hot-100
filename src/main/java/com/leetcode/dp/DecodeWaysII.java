package com.leetcode.dp;

/**
 * 639. 解码方法 II (Decode Ways II)
 *
 * 题目链接：https://leetcode.cn/problems/decode-ways-ii/
 * 难度：困难
 * 标签：动态规划、字符串
 *
 * ==================== 题目描述 ====================
 * 一条包含字母 A-Z 的消息通过以下的方式进行了编码：
 *
 * 'A' -> "1"
 * 'B' -> "2"
 * ...
 * 'Z' -> "26"
 *
 * 要解码一条已编码的消息，所有的数字都必须分组，然后按原来的编码方案反向映射回字母（可能存在多种方式）。
 * 例如，"11106" 可以映射为：
 *   - "AAJF" 对应分组 (1 1 10 6)
 *   - "KJF" 对应分组 (11 10 6)
 *
 * 注意，像 (1 11 06) 这样的分组是无效的，因为 "06" 不可以映射为 'F'，因为 "6" 与 "06" 不同。
 *
 * 除了上面描述的数字字母映射方案，编码消息中可能包含 '*' 字符，可以表示从 '1' 到 '9' 的任一数字（不包括 '0'）。
 * 例如，编码字符串 "1*" 可以表示 "11"、"12"、"13"、"14"、"15"、"16"、"17"、"18" 或 "19" 中的任意一条消息。
 * 对 "1*" 进行解码，相当于解码该字符串可以表示的任何编码消息。
 *
 * 给你一个字符串 s，由数字和 '*' 字符组成，返回解码该字符串的方法数目。
 * 由于答案数目可能非常大，返回 10^9 + 7 的模。
 *
 * 示例 1：
 * 输入：s = "*"
 * 输出：9
 * 解释：这一条编码消息可以表示 "1"、"2"、"3"、"4"、"5"、"6"、"7"、"8" 或 "9" 中的任意一条。
 * 可以分别解码成字符串 "A"、"B"、"C"、"D"、"E"、"F"、"G"、"H" 和 "I"。
 * 因此，"*" 总共有 9 种解码方法。
 *
 * 示例 2：
 * 输入：s = "1*"
 * 输出：18
 * 解释：这一条编码消息可以表示 "11"、"12"、"13"、"14"、"15"、"16"、"17"、"18" 或 "19" 中的任意一条。
 * 每种消息都可以由 2 种方法解码（例如，"11" 可以解码成 "AA" 或 "K"）。
 * 因此，"1*" 共有 9 * 2 = 18 种解码方法。
 *
 * 示例 3：
 * 输入：s = "2*"
 * 输出：15
 * 解释：这一条编码消息可以表示 "21"、"22"、"23"、"24"、"25"、"26"、"27"、"28" 或 "29" 中的任意一条。
 * "21"、"22"、"23"、"24"、"25" 和 "26" 由 2 种解码方法，但 "27"、"28" 和 "29" 仅有 1 种解码方法。
 * 因此，"2*" 共有 (6 * 2) + (3 * 1) = 12 + 3 = 15 种解码方法。
 *
 * 提示：
 * - 1 <= s.length <= 10^5
 * - s[i] 是 0-9 中的一位数字或字符 '*'
 *
 * ==================== 核心规则与约束条件 ====================
 * 1. 单数字解码：数字 1-9 可以单独解码为对应的字母 A-I。
 * 2. 双数字解码：数字 10-26 可以解码为对应的字母 J-Z。
 * 3. 关于 '0' 的限制：
 *    - 数字 0 不能单独解码为任何字母。
 *    - 0 不能作为双数字的开头（例如 "01"、"02" 等都是无效的）。
 * 4. 关于 '*' 的规则：
 *    - '*' 可以表示 1-9 中的任意一个数字（不包括 0）。
 *    - 单个 '*' 有 9 种解码方式。
 *    - 两个字符组合时，需要考虑所有可能的有效组合。
 * 5. 结果需要对 10^9 + 7 取模。
 *
 * ==================== 解题提示 ====================
 * 定义状态：dp[i] 表示字符串前 i 个字符的解码方法数
 * 转移方程：需要分情况讨论
 *   - 单个字符解码：
 *     * 如果 s[i-1] 是数字 1-9，dp[i] += dp[i-1]
 *     * 如果 s[i-1] 是 '*'，dp[i] += 9 * dp[i-1]
 *   - 两个字符解码：
 *     * 需要考虑 s[i-2] 和 s[i-1] 的所有组合情况
 *     * 数字+数字、数字+*、*+数字、*+* 四种情况
 * 初始条件：
 *   - dp[0] = 1（空字符串有一种解码方式）
 * 目标：dp[n] % (10^9 + 7)
 *
 * 注意事项：
 * - 需要特别处理 '*' 的各种组合情况
 * - 每一步都要对结果取模，防止溢出
 * - 可以使用滚动变量优化空间复杂度到 O(1)
 *
 * @Author Dxboy266
 * @Date 2025-11-07
 */
public class DecodeWaysII {

    private static final int MOD = 1000000007;

    /**
     * 解法一：动态规划（数组）
     * 时间复杂度：O(n)，其中 n 是字符串长度
     * 空间复杂度：O(n)
     *
     * @param s 待解码的字符串（包含数字和 '*'）
     * @return 解码方法数（对 10^9 + 7 取模）
     */
    public int numDecodings(String s) {
        if (s == null || s.length() == 0) {
            return 0;
        }

        int n = s.length();
        // 使用 long 防止中间计算溢出
        long[] dp = new long[n + 1];

        // 初始化
        dp[0] = 1;  // 空字符串
        dp[1] = ways1(s.charAt(0));  // 第一个字符

        // 如果第一个字符无法解码，直接返回 0
        if (dp[1] == 0) {
            return 0;
        }

        // 从第 2 个字符开始遍历
        for (int i = 2; i <= n; i++) {
            char curr = s.charAt(i - 1);  // 当前字符
            char prev = s.charAt(i - 2);  // 前一个字符

            // 选择1：单独解码当前字符
            dp[i] = (dp[i] + dp[i - 1] * ways1(curr)) % MOD;

            // 选择2：和前一个字符组合解码
            dp[i] = (dp[i] + dp[i - 2] * ways2(prev, curr)) % MOD;
        }

        return (int) dp[n];
    }

    /**
     * 解法二：动态规划（空间优化）
     * 时间复杂度：O(n)
     * 空间复杂度：O(1)
     *
     * 【优化思路】
     * 观察到 dp[i] 只依赖 dp[i-1] 和 dp[i-2]
     * 所以只需要保存最近的两个状态即可
     *
     * @param s 待解码的字符串（包含数字和 '*'）
     * @return 解码方法数（对 10^9 + 7 取模）
     */
    public int numDecodingsOptimized(String s) {
        if (s == null || s.length() == 0) {
            return 0;
        }

        int n = s.length();

        // 用两个变量代替数组
        long prev2 = 1;  // dp[i-2]
        long prev1 = ways1(s.charAt(0));  // dp[i-1]

        if (prev1 == 0) {
            return 0;
        }

        for (int i = 2; i <= n; i++) {
            char curr = s.charAt(i - 1);
            char prev = s.charAt(i - 2);

            long current = 0;  // dp[i]

            // 单独解码
            current = (current + prev1 * ways1(curr)) % MOD;

            // 组合解码
            current = (current + prev2 * ways2(prev, curr)) % MOD;

            // 滚动更新
            prev2 = prev1;
            prev1 = current;
        }

        return (int) prev1;
    }

    /**
     * 辅助方法：计算单个字符的解码方式数
     *
     * 【思路】
     * - '*' 可以表示 1-9，共 9 种
     * - '0' 不能单独解码，0 种
     * - '1'-'9' 各有 1 种
     *
     * @param c 字符
     * @return 解码方式数
     */
    private int ways1(char c) {
        if (c == '*') {
            return 9;  // '*' 可以是 1-9
        }
        if (c == '0') {
            return 0;  // '0' 不能单独解码
        }
        return 1;  // '1'-'9' 各有 1 种
    }

    /**
     * 辅助方法：计算两个字符组成的数字的解码方式数
     *
     * 【思路】需要分 4 种情况讨论：
     * 1. 数字 + 数字：判断是否在 [10, 26] 范围内
     * 2. 数字 + '*'：
     *    - '1*' → 11-19，共 9 种
     *    - '2*' → 21-26，共 6 种
     *    - 其他 → 0 种
     * 3. '*' + 数字：
     *    - '*0'-'*6' → 10/20, 11/21, ..., 16/26，共 2 种
     *    - '*7'-'*9' → 17, 18, 19，共 1 种
     * 4. '*' + '*'：
     *    - 11-19 (9种) + 21-26 (6种) = 15 种
     *
     * @param c1 第一个字符
     * @param c2 第二个字符
     * @return 解码方式数
     */
    private int ways2(char c1, char c2) {
        // 情况1：数字 + 数字
        if (c1 != '*' && c2 != '*') {
            int num = (c1 - '0') * 10 + (c2 - '0');
            return (num >= 10 && num <= 26) ? 1 : 0;
        }

        // 情况2：数字 + '*'
        if (c1 != '*' && c2 == '*') {
            if (c1 == '1') {
                return 9;  // 11-19
            }
            if (c1 == '2') {
                return 6;  // 21-26
            }
            return 0;  // 3*-9* 都超过 26
        }

        // 情况3：'*' + 数字
        if (c1 == '*' && c2 != '*') {
            if (c2 >= '0' && c2 <= '6') {
                return 2;  // 可以是 1x 或 2x
            }
            return 1;  // 只能是 1x（17-19）
        }

        // 情况4：'*' + '*'
        // 11-19 (9种) + 21-26 (6种) = 15 种
        return 15;
    }
}

